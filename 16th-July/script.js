let str = "Hello World";
console.log("Length:", str.length);
console.log("Uppercase:", str.toUpperCase());
console.log("Lowercase:", str.toLowerCase());
console.log("Trim:", str.trim());
console.log("Trim Start:", str.trimStart());
console.log("Concat:", str.concat("Welcome"));
console.log("Ends with 'Id':", str.endsWith("Id"));
console.log("Index of 'o':", str.lastIndexOf("o"));
console.log("Last Index of 'o':", str.lastIndexOf("o"));
console.log("Char At (2):", str.charAt(2));
console.log("Split by space:", str.trim().split(" "));
console.log("Repeat 3 times:", str.repeat(3));
console.log("Match 'World':", str.match(/World/));

